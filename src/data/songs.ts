// Helper function to get random songs by difficulty
export function getRandomSongsByDifficulty(difficulty: string, count: number): Song[] {
  let songs;
  switch (difficulty) {
    case 'easy':
      songs = [...easySongs, ...modernSongs.filter(s => s.difficulty === 'easy')];
      break;
    case 'medium':
      songs = [...mediumSongs, ...modernSongs.filter(s => s.difficulty === 'medium')];
      break;
    case 'hard':
      songs = hardSongs;
      break;
    default:
      songs = easySongs;
  }
  
  // Shuffle array using Fisher-Yates algorithm
  const shuffled = [...songs];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  
  return shuffled.slice(0, count);
}

// Helper function to get a random song based on player performance
export function getRandomSong(correctRate: number): Song {
  let difficulty;
  if (correctRate < 0.4) {
    difficulty = 'easy';
  } else if (correctRate < 0.7) {
    difficulty = 'medium';
  } else {
    difficulty = 'hard';
  }

  const songs = difficulty === 'easy' ? [...easySongs, ...modernSongs.filter(s => s.difficulty === 'easy')] :
                difficulty === 'medium' ? [...mediumSongs, ...modernSongs.filter(s => s.difficulty === 'medium')] :
                hardSongs;
  
  return songs[Math.floor(Math.random() * songs.length)];
}